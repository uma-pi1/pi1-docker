public class Octal {
  public static void main(String[] args) {
    System.out.println("11 " + 11);
    System.out.println("011 " + 011);
    System.out.println("0b11 " + 0b11);
    System.out.println("0x11 " + 0x11);
    System.out.println("-11 " + -11);
    System.out.println("-011 " + -011);
    System.out.println("-0b11 " + -0b11);
    System.out.println("-0x11 " + -0x11);
  }
}
